app.get('/read-file/fileName/:fileName/page/:page', function(req, res) {
    var fileName = req.params.fileName;
    var page = req.params.page;

    res.setHeader('Content-Type', 'application/json');

    if (!mockData[fileName]) {
        return res.send({ status: 404, fileName: fileName, page: page, message: 'File not found.' });
    }

    if (!mockData[fileName][page]) {
        return res.send({ status: 200, fileName: fileName, page: page, totalPage: mockData[fileName]['total'], content: [] });
    }

    if (page == 31) {
        setTimeout(function() {
            return res.send({ status: 200, fileName: fileName, page: page, totalPage: mockData[fileName]['total'], content: mockData[fileName][page] });
        }, 1000);
    }

    return res.send({ status: 200, fileName: fileName, page: page, totalPage: mockData[fileName]['total'], content: mockData[fileName][page] });
});

